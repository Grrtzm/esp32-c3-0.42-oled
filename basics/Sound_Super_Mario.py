# Calculate speed
tempo = 260  # BPM
wholenote = (60000 * 4) // tempo  # Duration of an entire note in ms

# Pin for the buzzer
buzzer = PWM(Pin(10))

def play_tone(frequency, duration):
    if frequency == REST:
        time.sleep_ms(duration)
    else:
        buzzer.freq(frequency)
        buzzer.duty_u16(32768)  # 50% duty cycle
        time.sleep_ms(duration)
        buzzer.duty_u16(0)  # Stop tone
        time.sleep_ms(duration // 10)  # Short break

# Play the melody
i = 0
while i < len(melody):
    note = melody[i]
    duration = melody[i + 1]

    # Calculate the duration in milliseconds
    if duration > 0:
        note_duration = wholenote // duration
    else:
        note_duration = (wholenote // abs(duration)) * 1.5  # Dotted note

    play_tone(note, int(note_duration))

    i += 2  # Ga naar de volgende noot en duur

# Zet de zoemer uit
buzzer.deinit()
